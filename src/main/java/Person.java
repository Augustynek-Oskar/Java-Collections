public class Person {
    String name;
    String surname;
    int yearOfBirth;
    float height;
    float weight;

    public Person(String name, String surname, int yearOfBirth, float height, float weight) {
        this.name = name;
        this.surname = surname;
        this.yearOfBirth = yearOfBirth;
        this.height = height;
        this.weight = weight;
    }
}
